CREATE OR REPLACE FUNCTION auth.get_user_by_phone(phone_input text)
 RETURNS TABLE(id uuid, email character varying, phone text)
 LANGUAGE plpgsql
 SECURITY DEFINER
AS $function$
BEGIN
    RETURN QUERY
    SELECT u.id, u.email, u.phone
    FROM auth.users u
    WHERE u.phone = phone_input;
END;
$function$
;


